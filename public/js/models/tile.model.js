define(
  [
    'underscore'
  , 'backbone'
  ]
, function(_, Backbone) {
  
  var model = {};
  
  /* ======================================================================= *
   *  MODEL ATTRIBUTES                                                       *
   * ======================================================================= */
  
  /* ======================================================================= *
   *  PUBLIC CLASS METHODS                                                   *
   * ======================================================================= */

  model.url = function() {
    if(this.get('id'))
      return 'tiles/' + this.get('id');
    else
      return 'tiles';
  }

  /* ======================================================================= *
   *  CONSTRUCTOR & INITIALIZATION                                           *
   * ======================================================================= */
  
  model.initialize = function () {
    _.bindAll(this);
  };
  
  /* ======================================================================= */
  /* ======================================================================= */
  
  return Backbone.Model.extend(model);
});
